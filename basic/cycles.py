"""
    Циклы — это конструкции, позволяющие выполнять блок кода многократно в зависимости от условия или итерации.
    В Python есть два основных типа циклов: for и while.
    Циклы позволяют автоматизировать повторяющиеся задачи, обходить коллекции, выполнять итерации и т.д.
"""

"""
    1 - цикл for
    2 - цикл while
    3 - ключевые слова break и continue
    4 - вложенные циклы
    5 - else в циклах
    6 - генераторы списков (с циклом for)
"""

### 1 - цикл for
"""
    Цикл for используется для итерации по элементам итерируемого объекта:
    строки, списки, кортежи, множества, словари и т.д.
    Синтаксис: for <переменная> in <итерируемый объект>:
"""
# Пример: перебор списка
numbers = [1, 2, 3, 4, 5]
for num in numbers:
    print(num) # 1, 2, 3, 4, 5

# Пример: перебор строки
text = "hello"
for char in text:
    print(char) # h, e, l, l, o

# Пример: использование range()
for i in range(3):
    print(i) # 0, 1, 2

# Пример: перебор словаря
data = {"name": "Alice", "age": 30}
for key in data:
    print(key, data[key]) # name Alice \n age 30

# Пример: перебор пар (ключ-значение)
for key, value in data.items():
    print(f"{key}: {value}") # name: Alice \n age: 30

### 2 - цикл while
"""
    Цикл while выполняется, пока условие истинно.
    Синтаксис: while <условие>:
    Важно обеспечить изменение переменной в теле цикла, чтобы избежать бесконечного цикла.
"""
# Пример: счётчик
i = 0
while i < 3:
    print(i) # 0, 1, 2
    i += 1 # важно изменять переменную, иначе будет бесконечный цикл

# Пример: цикл с пользовательским вводом (условно)
while True:
    user_input = input("Введите команду: ")
    if user_input == "quit":
        break

### 3 - ключевые слова break и continue
"""
    break — прерывает выполнение цикла полностью.
    continue — прерывает текущую итерацию и переходит к следующей.
"""
# Пример: break
for i in range(10):
    if i == 5:
        break
    print(i) # 0, 1, 2, 3, 4

# Пример: continue
for i in range(5):
    if i == 2:
        continue
    print(i) # 0, 1, 3, 4

### 4 - вложенные циклы
"""
    Циклы могут быть вложенными, то есть один цикл может находиться внутри другого.
    Используется, например, для работы с матрицами, таблицами и т.д.
"""
# Пример: вложенный цикл
for i in range(2):
    for j in range(2):
        print(f"({i}, {j})") # (0,0), (0,1), (1,0), (1,1)

### 5 - else в циклах
"""
    У циклов for и while может быть блок else.
    Он выполняется, если цикл завершается нормально (без break).
"""
# Пример: else в for
for i in range(3):
    print(i)
else:
    print("Цикл завершён нормально") # выполнится

# Пример: else с break (else не выполнится)
for i in range(3):
    if i == 1:
        break
    print(i)
else:
    print("Это не выполнится")

### 6 - генераторы списков (с циклом for)
"""
    Генераторы списков позволяют создавать списки в одну строку с помощью цикла for.
    Это компактный и эффективный способ обработки данных.
"""
# Пример: создание списка квадратов
squares = [x**2 for x in range(5)] # [0, 1, 4, 9, 16]

# Пример: с условием
evens = [x for x in range(10) if x % 2 == 0] # [0, 2, 4, 6, 8]

